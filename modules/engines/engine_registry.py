# =========================================================
# ENGINE REGISTRY
# =========================================================

class EngineRegistry:

    def __init__(self):

        self.engines = {}

    # =====================================================
    # REGISTER
    # =====================================================
    def register(

        self,

        name,

        engine
    ):

        self.engines[
            name
        ] = engine

    # =====================================================
    # GET ENGINE
    # =====================================================
    def get(

        self,

        name
    ):

        return self.engines.get(
            name
        )

    # =====================================================
    # GET ALL
    # =====================================================
    def get_all(self):

        return self.engines