# =========================================================
# STAGE MANAGER
# =========================================================

class StageManager:

    def __init__(self):

        self.stages = []

    # =====================================================
    # REGISTER
    # =====================================================
    def register(

        self,

        name,

        stage
    ):

        self.stages.append(
            (name, stage)
        )

    # =====================================================
    # GET STAGES
    # =====================================================
    def get_stages(self):

        return self.stages